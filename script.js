// Adresse du smart contract et ABI
const contractAddress = '0xA22605c95c4EA4AB90Af1180272b87D4c4837A9B'; // Remplacer par l'adresse de votre smart contract

// Création d'une instance du smart contract
const contract = new web3.eth.Contract(abi, contractAddress);

// Fonction pour appeler le smart contract
document.addEventListener('DOMContentLoaded', () => {
  
  var $formId = document.getElementById ('formName'); 
  var $maxForm = document.getElementById('formMax');
  var $formBalance = document.getElementById('formBalance');
  var $formTranf = document.getElementById('formTranf');
  
  var $name = document.getElementById('name');
  var $max  = document.getElementById('max');
  var $balance = document.getElementById('balance');
  var $inputBalance = document.getElementById('inputBalance');
  var $addressReceive = document.getElementById('addressReceive');
  var $amountReceive = document.getElementById('amountReceive');
  var $resulTransf = document.getElementById('resulTransf');
  
  $formId.addEventListener('submit', event =>{
    event.preventDefault();
    contract.methods.getName().call()
      .then(result =>{$name.innerHTML = result}); 
    
  });
  $maxForm.addEventListener('submit', event =>{
     event.preventDefault();
     contract.methods.getMaxSuply().call()
         .then(response => {$max.innerHTML = response;})
  }); 

  $formBalance.addEventListener('submit', event => {
      event.preventDefault();
      contract.methods.getBalanceOf(""+$inputBalance.value).call()
         .then(resp => {$balance.innerHTML = resp;})
  });

  $formTranf.addEventListener('submit', event =>{
      event.preventDefault();
      contract.methods.sendToken($addressReceive.value,$amountReceive.value).send({from: "0x24877cB1F286cD9E1b8d86d0DCefEC9507D56686"})
        .then(resp => {$resulTransf.innerHTML = " Successfuly token transaction !";})
  });


})


